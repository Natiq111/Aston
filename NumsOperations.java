import java.util.Scanner;

public class Main {

    public static void main(String[] args) {

        // Input
        Scanner scanner = new Scanner(System.in);

        System.out.print("Enter a: ");
        int a = scanner.nextInt();
        System.out.print("Enter b: ");
        int b = scanner.nextInt();

        scanner.close();

        if (a > b) {
            System.out.println(a + " > " + b);
        } else if (a < b) {
            System.out.println(a + " < " + b);
        } else {
            System.out.println(a + " = " + b);
        }

        // Operations
        double sum = a + b;
        double divorce = a - b;
        double division = a / b;
        double multiplication = a * b;

        // Output
        System.out.printf("Result of the operations:%n sum=%f%n divorce=%f%n division=%f%n multiplication=%f%n", sum, divorce, division, multiplication);

    }

}
